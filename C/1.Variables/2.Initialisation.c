/*

Après avoir déclaré une variabale, il est nécessaire de l'initialisé (donner une 1ère valeur) parmis les façons qui suivent :

*/

//Déclaration puis initialisation
type identificateur;
identificateur = valeur;

//Déclaration et initialisation
type identificateur = valeur;

//Déclaration de plusieurs vairables puis initialisation
type identificateur1, identificateur2;
identificateur1 = 50;
identificateur2 = 100;

/*

Voici les différentes tips à connaitre en fonction des types (Déclaration et initialisation, ici) :

*/

// Initialisation d'un char de deux manières identiques
char myChar = 'A';
char myChar = 65;

// Initialisation d'un int
int myInt = 65;

// Initialisation d'un float
float myFloat = 0.65;

// Initialisation d'un double
double myFLoat = 0.65;

double myFLoat = 65E-2; /* 1x10^-1 = 6.5 */

//Initialisation d'un booléen à vrai
_Bool myBool = 1;

//Initialisation d'un booléen à faux
_Bool myBool = 0;

/*

Le principe reste le même en utilisant signe et unsigned, il suffit de choisir une valeur comprise dans la plage des valeurs.

*/


