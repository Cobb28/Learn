/*

Il existe un opérateur spécifique pour décrementer une variable : --.

*/

int var = 1;

var--; // var = 0

/*

Il est possible d'utiliser l'opérateur comme suffixe/préfixe.

*/

int var = 1;

--var; // var = 0



/*

    Dans le cas de l’opérateur préfixé (--variable ou ++variable), le résultat sera la valeur de la variable augmentée ou diminuée d’une unité.
    Dans le cas de l’opérateur suffixé (variable-- ou variable++), le résultat sera la valeur de la variable.

*/